    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if (idProductoActual == -1) {
            JOptionPane.showMessageDialog(this, "Primero busca un producto válido.");
            return;
        }

        if (cbxCampo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Selecciona un campo para actualizar.");
            return;
        }

        try {
            String campo = cbxCampo.getSelectedItem().toString();
            String valor = txtNuevoValor.getText();

            ProductoControlador pc = new ProductoControlador();
            if (pc.actualizarCampoProducto(idProductoActual, campo, valor)) {
                JOptionPane.showMessageDialog(this, "Producto actualizado.");
                cargarTablaProductos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (idProductoActual == -1) {
            JOptionPane.showMessageDialog(this, "Primero busca un producto válido.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de eliminar este producto?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            ProductoControlador pc = new ProductoControlador();
            if (pc.eliminarProducto(idProductoActual)) {
                JOptionPane.showMessageDialog(this, "Producto eliminado.");
                cargarTablaProductos();
                // Limpiar campos después de eliminar
                txtIdProducto.setText("");
                txtNuevoValor.setText("");
                idProductoActual = -1;
                btnActualizar.setEnabled(false);
                btnEliminar.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

