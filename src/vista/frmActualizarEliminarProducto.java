    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = txtIdProducto.getText().trim();

        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa el código del producto (por ejemplo: PR0005).");
            return;
        }

        ProductoControlador pc = new ProductoControlador();
        Producto p = pc.obtenerProductoPorCodigo(codigo);

        if (p != null) {
            idProductoActual = p.getId(); // Guardamos el ID numérico
            txtNuevoValor.setText(p.getNombre());
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Producto no encontrado.");
            idProductoActual = -1;
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

